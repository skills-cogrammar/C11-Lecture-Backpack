# Data Types and examples

# 1. **Integer (int)**:
#    - Represents whole numbers (positive or negative) without decimals.
#    - Used for counting and indexing.
a = 1
b = -1
print(a, b)  # Output: 1 -1

# 2. **Float (float)**:
#    - Represents numbers with decimals.
#    - Used for measurements or continuous data.
pi = 3.14
negative_float = -1.0
print(pi, negative_float)  # Output: 3.14 -1.0

# 3. **Boolean (bool)**:
#    - Represents True or False values.
#    - Used for logical conditions and decision-making.
is_active = True
is_inactive = False
print(is_active, is_inactive)  # Output: True False

# 4. **String (str)**:
#    - Represents a sequence of characters (text).
#    - All input from the user is always in a string format.
#    - Immutable (cannot be changed after creation).
greeting = "Hello"
print(greeting)  # Output: Hello

# 5. **List (list)**:
#    - Represents an ordered collection of items.
#    - Ordered - The collections keeps the order in which items were added.
#    - Items can be of any data type and are mutable (can be changed).
numbers = [1, 2]
print(numbers)  # Output: [1, 2]

# 6. **Set (set)**:
#    - Represents an unordered collection of unique items.
#    - Used to store distinct values and supports set operations like union and intersection.
mixed_set = {1, 'a'}
print(mixed_set)  # Output: {1, 'a'}

# 7. **Tuple (tuple)**:
#    - Represents an ordered collection of items.
#    - Immutable (cannot be changed after creation).
coordinates = (1.0, 0)
print(coordinates)  # Output: (1.0, 0)

# 8. **Dictionary (dict)**:
#    - Represents a collection of key-value pairs.
#    - Keys must be unique, and values can be of any data type.
point = {'x': 0.1, 'y': 0.2}
print(point)  # Output: {'x': 0.1, 'y': 0.2}

# Each of these examples shows how to create and print out the corresponding data type in Python.

